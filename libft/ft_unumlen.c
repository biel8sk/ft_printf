#include "libft.h"

size_t	ft_unumlen(unsigned int n)
{
	size_t	len;

	len = 1;
	if (n < 0)
	{
		len++;
	}
	while (n > 9)
	{
		n /= 10;
		len ++;
	}
	return (len);
}
